//******************************************************************************
//
//! @brief  检查播放状态
//!
//! @param  None
//! @retval uint8_t - 播放状态(1:正在播放, 0:停止)
//!
//! @note   查询蜂鸣器当前播放状态
//! @remark 返回值为1表示正在播放且未暂停
//
//******************************************************************************
uint8_t Buzzer_is_playing(void)
{
  return is_playing && !is_paused;
}

//******************************************************************************
//
//! @brief  蜂鸣器播放任务实现
//!
//! @note   执行流程：
//!         1. 等待播放命令
//!         2. 根据命令类型执行相应操作
//!         3. 处理音符播放和延时
//!
//! @remark 运行在FreeRTOS任务上下文中
//
//******************************************************************************
void Buzzer_play_task(Buzzer_message_t *Buzzer_message)
{
      switch (Buzzer_message->cmd)
      {
      case BUZZER_CMD_PLAY_NOTE:
        if (!is_paused)
        {
          is_playing = 1;

          if (Buzzer_message->note.frequency > 0)
          {
            Buzzer_set_frequency(Buzzer_message->note.frequency);
          }
          else
          {
            Buzzer_stop();
          }
          vTaskDelay(pdMS_TO_TICKS(Buzzer_message->note.period));
          Buzzer_stop();
          is_playing = 0;
        }
        break;

      case BUZZER_CMD_PLAY_SONG:
          is_playing = 1;
          is_paused = 0;

        while (current_count < Buzzer_message->song.note_count)
        {
          if (!is_paused)
            {
              const Buzzer_note_t Current_note = Buzzer_message->song.notes[current_count];
              uint32_t duration = Calculate_note_duration(Current_note.period, Buzzer_message->song.tempo);

              if (Current_note.frequency > 0)
              {
                  Buzzer_set_frequency(Current_note.frequency);
              }
              else
              {
                  Buzzer_stop();
              }

              if (duration > 5)
              {
                vTaskDelay(pdMS_TO_TICKS(75));
                vTaskDelay(pdMS_TO_TICKS(duration - 5 - 75));
                Buzzer_set_frequency(0);
                vTaskDelay(pdMS_TO_TICKS(5));
              }
              else
              {
                vTaskDelay(pdMS_TO_TICKS(duration));
              }
              current_count++;
            }
            else
            {
              vTaskDelay(pdMS_TO_TICKS(10));
            }
          if (Buzzer_message->cmd == Buzzer_CMD_PAUSE)
          {
            break;
          }
        }
        current_count = 0;
        Buzzer_stop();
        is_playing = 0;
        break;
      }

  }

