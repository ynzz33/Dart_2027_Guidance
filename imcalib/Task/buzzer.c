//******************************************************************************
//
//! @brief  设置PWM频率实现
//!
//! @note   执行流程：
//!         1. 频率范围检查
//!         2. 计算ARR和CCR值
//!         3. 配置定时器参数
//!         4. 重置计数器
//!
//! @remark 使用20%占空比以获得最佳蜂鸣器效果
//
//******************************************************************************
void Buzzer_set_frequency(uint16_t freq){
  if (freq < BUZZER_MIN_FREQ || freq > BUZZER_MAX_FREQ) {
    __HAL_TIM_SET_COMPARE(&htim2, TIM_CHANNEL_2, 0);
    return;
  }

  uint32_t arr = (BUZZER_SYS_CLOCK / (BUZZER_TIM_PRESCALER * freq)) - 1;
  uint32_t ccr = arr / 5;
  if (ccr == 0) ccr = 1;

  __HAL_TIM_SET_PRESCALER(&htim2, BUZZER_TIM_PRESCALER);
  __HAL_TIM_SET_AUTORELOAD(&htim2, arr);
  __HAL_TIM_SET_COMPARE(&htim2, TIM_CHANNEL_2, ccr);
  __HAL_TIM_SET_COUNTER(&htim2, 0);
}

//******************************************************************************
//
//! @brief  计算音符持续时间实现
//!
//! @note   根据节拍速度计算音符的实际播放时长
//!
//! @remark 公式：duration = (period * 60 * 1000) / bpm
//
//******************************************************************************
static uint32_t Calculate_note_duration(float period, uint16_t bpm){
  float beat_duration = (60.0f / (float)bpm) * 1000.0f;
  return (uint32_t)(period * beat_duration);
}
//******************************************************************************
//
//! @brief  播放单个音符
//!
//! @param  [in] freq - 音符频率(Hz)
//! @param  [in] duration_ms - 持续时间(ms)
//! @retval None
//!
//! @note   非阻塞播放指定频率的单音
//! @remark 通过FreeRTOS队列实现异步播放
//
//******************************************************************************
void Buzzer_play_note(uint16_t frequency, uint16_t duration_ms)
{
  Buzzer_message.cmd = BUZZER_CMD_PLAY_NOTE;
  Buzzer_message.note.frequency = frequency;
  Buzzer_message.note.period = duration_ms;

}

//******************************************************************************
//
//! @brief  停止播放
//!
//! @param  None
//! @retval None
//!
//! @note   立即停止当前播放并静音
//! @remark 会中断正在播放的音符或歌曲
//
//******************************************************************************
void Buzzer_stop(void)
{

  Buzzer_message.cmd = Buzzer_CMD_PAUSE;
  // 立即停止PWM输出
  __HAL_TIM_SET_COMPARE(&htim2, TIM_CHANNEL_2, 0);
}

